# ══════════════════════════════════════════════════════════════════════════════
#  EVALUATION DIMENSIONS (as per paper)
# ══════════════════════════════════════════════════════════════════════════════

def _evaluate_dimensions(question: str, candidate_answer: str,
                          expected: str, similarity: float,
                          question_type: str) -> dict:
    """
    Paper: "Response evaluation employs multiple analytical dimensions"
    1. Content relevance  — does answer address the question?
    2. Completeness       — is the answer elaborated enough?
    3. Language quality   — is it clear and professional?
    4. Keyword coverage   — does it include key concepts?
    """
    minilm = _get_minilm()

    # 1. Content relevance — similarity between answer and question
    q_emb   = minilm.encode(question,          convert_to_tensor=True)
    ans_emb = minilm.encode(candidate_answer,  convert_to_tensor=True)
    content_score = round(float(util.cos_sim(q_emb, ans_emb)[0][0]) * 100, 1)

    # 2. Completeness — word count as proxy for elaboration
    word_count = len(candidate_answer.split())
    if word_count >= 80:
        completeness = "Complete"
        completeness_score = 100
    elif word_count >= 40:
        completeness = "Adequate"
        completeness_score = 70
    elif word_count >= 15:
        completeness = "Brief"
        completeness_score = 40
    else:
        completeness = "Too Short"
        completeness_score = 10

    # 3. Language quality — check for filler words, professional tone
    filler_words = ["um", "uh", "like", "basically", "literally", "you know"]
    filler_count = sum(candidate_answer.lower().count(w) for w in filler_words)
    language_score = max(0, 100 - (filler_count * 10))

    # 4. Keyword coverage — key terms from expected answer found in candidate answer
    expected_words    = set(expected.lower().split())
    candidate_words   = set(candidate_answer.lower().split())
    # Filter meaningful words (length > 4)
    meaningful_expected  = {w for w in expected_words   if len(w) > 4}
    meaningful_candidate = {w for w in candidate_words  if len(w) > 4}
    if meaningful_expected:
        keyword_coverage = round(
            len(meaningful_expected & meaningful_candidate) / len(meaningful_expected) * 100, 1
        )
    else:
        keyword_coverage = 50.0

    return {
        "content_relevance": {
            "score":       content_score,
            "label":       _score_label(content_score),
            "description": "How well the answer addresses the question",
        },
        "completeness": {
            "score":       completeness_score,
            "label":       completeness,
            "word_count":  word_count,
            "description": "Whether the answer is elaborated sufficiently",
        },
        "language_quality": {
            "score":       language_score,
            "label":       _score_label(language_score),
            "description": "Clarity and professional tone of the answer",
        },
        "keyword_coverage": {
            "score":       keyword_coverage,
            "label":       _score_label(keyword_coverage),
            "description": "Key concepts from expected answer covered",
        },
    }


# ══════════════════════════════════════════════════════════════════════════════
#  RATING + FEEDBACK
# ══════════════════════════════════════════════════════════════════════════════

def _get_rating(similarity: float) -> str:
    """Paper scoring thresholds."""
    if similarity >= 0.90: return "Excellent"
    if similarity >= 0.75: return "Good"
    if similarity >= 0.60: return "Average"
    return "Poor"


def _generate_feedback(rating: str, dimensions: dict,
                        skill: str, question_type: str) -> dict:
    """
    Paper: "generates explanatory feedback highlighting what aspects
            worked well and which require improvement"
    """
    strengths = []
    improvements = []

    # Content
    content_score = dimensions["content_relevance"]["score"]
    if content_score >= 70:
        strengths.append("Your answer is relevant and addresses the question well.")
    else:
        improvements.append("Try to more directly address what the question is asking.")

    # Completeness
    comp_label = dimensions["completeness"]["label"]
    word_count = dimensions["completeness"]["word_count"]
    if comp_label in ["Complete", "Adequate"]:
        strengths.append(f"Good level of detail with {word_count} words.")
    else:
        improvements.append(
            f"Your answer is too brief ({word_count} words). "
            f"Aim for at least 50-80 words with specific examples."
        )

    # Keywords
    kw_score = dimensions["keyword_coverage"]["score"]
    if kw_score >= 60:
        strengths.append("You covered the key concepts expected in the answer.")
    else:
        improvements.append(
            f"Include more specific terminology related to {skill}. "
            f"Review core concepts and use proper technical vocabulary."
        )

    # Language
    lang_score = dimensions["language_quality"]["score"]
    if lang_score >= 80:
        strengths.append("Clear and professional communication style.")
    else:
        improvements.append("Avoid filler words and maintain a professional tone.")

    # Overall tip based on question type
    type_tips = {
        "technical":    "For technical questions, include examples, code concepts, or real use cases.",
        "behavioral":   "Use the STAR method: Situation → Task → Action → Result.",
        "situational":  "Structure your answer: identify the issue → list steps → explain outcome.",
        "problem_solving": "Walk through your thought process step by step.",
    }

    return {
        "overall_rating": rating,
        "strengths":      strengths,
        "improvements":   improvements,
        "tip":            type_tips.get(question_type, "Be specific and structured."),
    }


def _overall_summary(avg_score: float, results: list) -> dict:
    """Summary across all answers."""
    excellent = sum(1 for r in results if r["rating"] == "Excellent")
    good      = sum(1 for r in results if r["rating"] == "Good")
    average   = sum(1 for r in results if r["rating"] == "Average")
    poor      = sum(1 for r in results if r["rating"] == "Poor")

    if avg_score >= 90:
        verdict = "Outstanding performance! You are well prepared for this interview."
    elif avg_score >= 75:
        verdict = "Good performance. Review the areas marked for improvement."
    elif avg_score >= 60:
        verdict = "Average performance. Focus on providing more detailed and structured answers."
    else:
        verdict = "Needs improvement. Practice answering with specific examples and technical depth."

    return {
        "verdict":   verdict,
        "breakdown": {
            "excellent": excellent,
            "good":      good,
            "average":   average,
            "poor":      poor,
        },
    }


def _score_label(score: float) -> str:
    if score >= 85: return "Excellent"
    if score >= 70: return "Good"
    if score >= 50: return "Average"
    return "Poor"