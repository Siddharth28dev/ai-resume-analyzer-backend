"""
evaluation_service.py
──────────────────────
Phase 3B — Candidate Answer Evaluation using MiniLM

Model: sentence-transformers/all-MiniLM-L6-v2

How it works:
  1. For each question, an expected answer is generated by FLAN-T5
  2. Candidate submits their answer (text)
  3. MiniLM encodes both answers into embeddings
  4. Cosine similarity computed between them

Scoring:
  0.90+ → Excellent
  0.75+ → Good
  0.60+ → Average
  < 0.60 → Poor

Paper reference:
  "Response evaluation employs multiple analytical dimensions.
   Content analysis assesses whether answers address the question asked,
   provide specific examples, and demonstrate relevant competencies."
"""

import torch
from sentence_transformers import SentenceTransformer, util
from transformers import T5ForConditionalGeneration, T5Tokenizer


# ── MiniLM singleton ──────────────────────────────────────────────────────────
_minilm        = None
_t5_model      = None
_t5_tokenizer  = None


def _get_minilm() -> SentenceTransformer:
    global _minilm
    if _minilm is not None:
        return _minilm
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"[MiniLM] Loading all-MiniLM-L6-v2 on {device}...")
    _minilm = SentenceTransformer("all-MiniLM-L6-v2", device=device)
    print(f"[MiniLM] Ready")
    return _minilm


def _get_t5():
    global _t5_model, _t5_tokenizer
    if _t5_model is not None:
        return _t5_model, _t5_tokenizer
    device = "cuda" if torch.cuda.is_available() else "cpu"
    print(f"[FLAN-T5] Loading for expected answer generation...")
    _t5_tokenizer = T5Tokenizer.from_pretrained("google/flan-t5-base")
    _t5_model     = T5ForConditionalGeneration.from_pretrained(
        "google/flan-t5-base"
    ).to(device)
    _t5_model.eval()
    return _t5_model, _t5_tokenizer


# ══════════════════════════════════════════════════════════════════════════════
#  PUBLIC API
# ══════════════════════════════════════════════════════════════════════════════

def evaluate_answer(
    question: str,
    candidate_answer: str,
    question_type: str = "technical",
    skill: str = "",
    job_role: str = "",
) -> dict:
    """
    Evaluate a single candidate answer.

    Args:
        question         : The interview question asked
        candidate_answer : Answer given by the candidate
        question_type    : technical / behavioral / situational
        skill            : Skill being tested (for expected answer generation)
        job_role         : Job role context

    Returns:
        score, rating, feedback, expected_answer, similarity
    """
    if not candidate_answer or len(candidate_answer.strip()) < 10:
        return {
            "score":           0.0,
            "rating":          "No Answer",
            "feedback":        "No answer was provided.",
            "similarity":      0.0,
            "expected_answer": "",
            "dimensions":      {},
        }

    minilm = _get_minilm()

    # Step 1: Generate expected answer using FLAN-T5
    expected = _generate_expected_answer(question, question_type, skill, job_role)

    # Step 2: Encode both answers with MiniLM
    expected_emb  = minilm.encode(expected,          convert_to_tensor=True)
    candidate_emb = minilm.encode(candidate_answer,  convert_to_tensor=True)

    # Step 3: Cosine similarity
    similarity = float(util.cos_sim(expected_emb, candidate_emb)[0][0])
    similarity = round(similarity, 4)

    # Step 4: Score on 0-100 scale
    score = round(similarity * 100, 1)

    # Step 5: Multiple evaluation dimensions (as per paper)
    dimensions = _evaluate_dimensions(
        question, candidate_answer, expected, similarity, question_type
    )

    # Step 6: Rating + feedback
    rating   = _get_rating(similarity)
    feedback = _generate_feedback(rating, dimensions, skill, question_type)

    return {
        "score":           score,
        "similarity":      similarity,
        "rating":          rating,
        "expected_answer": expected,
        "feedback":        feedback,
        "dimensions":      dimensions,
    }


def evaluate_multiple_answers(answers: list) -> dict:
    """
    Evaluate multiple question-answer pairs at once.

    Args:
        answers: list of dicts with keys:
                 question, candidate_answer, question_type, skill, job_role

    Returns:
        overall score + individual results
    """
    results     = []
    total_score = 0.0

    for item in answers:
        result = evaluate_answer(
            question         = item.get("question", ""),
            candidate_answer = item.get("candidate_answer", ""),
            question_type    = item.get("question_type", "technical"),
            skill            = item.get("skill", ""),
            job_role         = item.get("job_role", ""),
        )
        result["question"] = item.get("question", "")
        results.append(result)
        total_score += result["score"]

    avg_score = round(total_score / len(results), 1) if results else 0.0

    return {
        "overall_score":       avg_score,
        "overall_rating":      _get_rating(avg_score / 100),
        "total_questions":     len(results),
        "individual_results":  results,
        "summary":             _overall_summary(avg_score, results),
    }


